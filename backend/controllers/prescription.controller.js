import Prescription from '../models/Prescription.js';
import PDFDocument from 'pdfkit';

// Nai prescription likho
export const createPrescription = async (req, res) => {
  try {
    const { patientId, medicines, diagnosis, instructions, followUpDate } = req.body;

    const prescription = await Prescription.create({
      patientId,
      doctorId: req.user._id,
      medicines,
      diagnosis,
      instructions,
      followUpDate,
    });

    res.status(201).json({ success: true, prescription });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// Patient ki sab prescriptions
export const getPatientPrescriptions = async (req, res) => {
  try {
    const prescriptions = await Prescription.find({
      patientId: req.params.patientId
    })
      .populate('doctorId', 'name')
      .sort({ createdAt: -1 });

    res.json({ success: true, prescriptions });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// Ek prescription detail
export const getPrescriptionById = async (req, res) => {
  try {
    const prescription = await Prescription.findById(req.params.id)
      .populate('doctorId', 'name')
      .populate('patientId', 'name age gender');

    if (!prescription)
      return res.status(404).json({ message: 'Prescription nahi mili' });

    res.json({ success: true, prescription });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// PDF Generate
export const generatePrescriptionPDF = async (req, res) => {
  try {
    const rx = await Prescription.findById(req.params.id)
      .populate('doctorId', 'name email')
      .populate('patientId', 'name age gender contact');

    if (!rx) return res.status(404).json({ message: 'Prescription nahi mili' });

    const doc = new PDFDocument({ margin: 50 });
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=prescription-${rx._id}.pdf`);
    doc.pipe(res);

    // Header
    doc.fontSize(22).fillColor('#1d4ed8').text('MediCare Clinic', { align: 'center' });
    doc.fontSize(10).fillColor('#6b7280').text('AI-Powered Clinic Management System', { align: 'center' });
    doc.moveDown();
    doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor('#e5e7eb').stroke();
    doc.moveDown();

    // Patient + Doctor Info
    doc.fontSize(11).fillColor('#111827');
    doc.text(`Patient: ${rx.patientId?.name}`, 50);
    doc.text(`Age / Gender: ${rx.patientId?.age} yrs / ${rx.patientId?.gender}`);
    doc.text(`Contact: ${rx.patientId?.contact}`);
    doc.moveDown(0.5);
    doc.text(`Doctor: Dr. ${rx.doctorId?.name}`);
    doc.text(`Date: ${new Date(rx.createdAt).toLocaleDateString('en-PK', {
      day: 'numeric', month: 'long', year: 'numeric'
    })}`);
    doc.moveDown();

    // Diagnosis
    doc.fontSize(13).fillColor('#1d4ed8').text('Diagnosis');
    doc.fontSize(11).fillColor('#111827').text(rx.diagnosis);
    doc.moveDown();

    // Medicines
    doc.fontSize(13).fillColor('#1d4ed8').text('Medicines');
    doc.moveDown(0.3);
    rx.medicines.forEach((m, i) => {
      doc.fontSize(11).fillColor('#111827')
        .text(`${i + 1}. ${m.name} — ${m.dosage}`, { continued: false });
      doc.fontSize(10).fillColor('#6b7280')
        .text(`   Frequency: ${m.frequency}  |  Duration: ${m.duration}`);
      if (m.notes) doc.text(`   Note: ${m.notes}`);
      doc.moveDown(0.3);
    });

    // Instructions
    if (rx.instructions) {
      doc.moveDown(0.5);
      doc.fontSize(13).fillColor('#1d4ed8').text('Instructions');
      doc.fontSize(11).fillColor('#111827').text(rx.instructions);
    }

    // Follow-up
    if (rx.followUpDate) {
      doc.moveDown();
      doc.fontSize(11).fillColor('#d97706')
        .text(`Follow-up Date: ${new Date(rx.followUpDate).toLocaleDateString('en-PK')}`);
    }

    // Footer
    doc.moveDown(2);
    doc.moveTo(50, doc.y).lineTo(550, doc.y).strokeColor('#e5e7eb').stroke();
    doc.moveDown(0.5);
    doc.fontSize(9).fillColor('#9ca3af')
      .text('Generated by MediCare SaaS — AI Clinic Management System', { align: 'center' });

    doc.end();
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};